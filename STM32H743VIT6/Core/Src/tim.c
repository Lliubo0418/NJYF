void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
if (htim->Instance == TIM2)
  {

    if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_3)
    {
      if (Int4_first_flag == 0)
      {
        Int4_starttime = HAL_TIM_ReadCapturedValue(&htim2, TIM_CHANNEL_3);
//				TIM_RESET_CAPTUREPOLARITY(&htim2, TIM_CHANNEL_3);
				__HAL_TIM_SET_CAPTUREPOLARITY(&htim2, TIM_CHANNEL_3, TIM_ICPOLARITY_FALLING); // 设置为下降沿捕获
        Int4_first_flag = 1;
      }
      else
      {
        Int4_endtime = HAL_TIM_ReadCapturedValue(&htim2, TIM_CHANNEL_3);
//				TIM_RESET_CAPTUREPOLARITY(&htim2, TIM_CHANNEL_3);
				__HAL_TIM_SET_CAPTUREPOLARITY(&htim2, TIM_CHANNEL_3, TIM_ICPOLARITY_RISING); // 设置为上升沿捕获
        Int4_first_flag = 0;
        if (Int4_endtime <= Int4_starttime)
        {
          Int4_duration = 500000 + Int4_endtime - Int4_starttime;
        }
        else
        {
          Int4_duration = Int4_endtime - Int4_starttime;
        }
      }
		}
	}
}
