/* Systick功能实现us延时，参数SYSCLK为系统时钟 */
uint32_t fac_us;
void HAL_Delay_us_init(uint8_t SYSCLK) {
    fac_us = SYSCLK;
}

void HAL_Delay_us(uint32_t nus) {
    uint32_t ticks;
    uint32_t told, tnow, tcnt = 0;
    uint32_t reload = SysTick->LOAD;
    ticks = nus * fac_us;
    told = SysTick->VAL;
    while (1) {
        tnow = SysTick->VAL;
        if (tnow != told) {
            if (tnow < told) tcnt += told - tnow;
            else tcnt += reload - tnow + told;
            told = tnow;
            if (tcnt >= ticks) break;
        }
    }
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if(htim->Instance == TIM4)
    {
        switch(pulse_cnt)
        {
            case 0:
                __HAL_TIM_SetCompare(&htim4, TIM_CHANNEL_1, 5);       //5个全满脉冲后吃进去一个2us脉冲
                break;
            case 5:
							__HAL_TIM_SetCompare(&htim4, TIM_CHANNEL_1, 2);     //TIM4:SYNC  TIM2:PULSE
                break;
						case 6:                              //开启pulse脉冲
								__HAL_TIM_SetCompare(&htim2, TIM_CHANNEL_2, 2);      
								__HAL_TIM_SetCompare(&htim4, TIM_CHANNEL_1, 0);
						//HAL_TIM_PWM_Start(&htim2,TIM_CHANNEL_2);
							break;
						case 7:		
								__HAL_TIM_SetCompare(&htim4, TIM_CHANNEL_1, 2);
								break;
            case 39:                             //关闭pulse								
                __HAL_TIM_SetCompare(&htim4, TIM_CHANNEL_1, 0);
								__HAL_TIM_SetCompare(&htim2, TIM_CHANNEL_2, 0);
								//HAL_TIM_PWM_Stop(&htim2,TIM_CHANNEL_2);
                break;
        }
        pulse_cnt++;
        if(pulse_cnt == 60)
        {
            pulse_cnt = 0;
        }
    }
}

