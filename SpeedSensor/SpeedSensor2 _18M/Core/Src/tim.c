void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
  BaseType_t xHigherPriorityTaskWoken;
  static float previousFrequency = 0; // 存储上一次的频率
  static uint8_t isSecondCapture = 0; // 标志位，用于判断是否是第二次捕获
  float currentFrequency = 0;         // 存储当前计算的频率
  if (htim->Instance == TIM2)
  {
    if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_3)
    {
      __HAL_TIM_SET_COUNTER(&htim4, 0); // 检测是否有输入
      if (IsFisrtEdge)
      {
        CaptureEdge1Time = HAL_TIM_ReadCapturedValue(&htim2, TIM_CHANNEL_3);
        IsFisrtEdge = 0;
      }
      else
      {
        //      CaptureEdge2Time = __HAL_TIM_GetCounter(htim);
        CaptureEdge2Time = HAL_TIM_ReadCapturedValue(&htim2, TIM_CHANNEL_3);
        // LTODO:后续计算可以放在任务中执行
        if (CaptureEdge2Time > CaptureEdge1Time)
        {
          currentFrequency = (100000.0 / (CaptureEdge2Time - CaptureEdge1Time)); // Calculate frequency in Hz
        }
        else
        {
          currentFrequency = (100000.0 / (0xFFFF - CaptureEdge1Time + CaptureEdge2Time)); // Calculate frequency in Hz    周期500ms
        }
        // LXXX:是否考虑干扰，是否加前后判断
        //  取两次频率的平均值
        if (isSecondCapture)
        {
          // 计算两次的平均值
          Frequency = (currentFrequency + previousFrequency) / 2;

          // 上报或通知任务
          vTaskNotifyGiveFromISR(Freq_ICTaskHandle, &xHigherPriorityTaskWoken);
          portYIELD_FROM_ISR(xHigherPriorityTaskWoken);

          // 重置标志位
          isSecondCapture = 0;
        }
        else
        {
          // 保存当前频率到 previousFrequency
          previousFrequency = currentFrequency;

          // 设置标志位，等待下一次捕获
          isSecondCapture = 1;
        }

        IsFisrtEdge = 1;
      }
    }
  }
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  BaseType_t xHigherPriorityTaskWoken;
  if (htim->Instance == TIM1) {
    HAL_IncTick();
  }
  else if (htim->Instance == TIM4)
  {
    Frequency = 0.0;            
    if (xTaskGetSchedulerState() != taskSCHEDULER_NOT_STARTED)
    {
      vTaskNotifyGiveFromISR(Freq_ICTaskHandle, &xHigherPriorityTaskWoken);
      portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
    }
  }
}
